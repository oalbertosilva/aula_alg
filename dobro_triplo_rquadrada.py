## O Objetivo desse exercício é receber um número inteiro e mostrar o seu dobro, triplo e raiz quadra ###

n1 = int(input("Digite um número inteiro: "))
dobro = n1*2
triplo = n1*3
rquadrada = n1**(1/2)
print("O dobro do número digitado é: ", dobro, "o triplo: ",
      triplo, "e a raiz quadrada é: ", rquadrada)
print("Fim do programa!")
